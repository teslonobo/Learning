# Find the min and max value from a group of numbers
a = min(2,4,6,8,10)
b = max(2,4,6,8,10)

print(a)
print(b)

# Find the absolute value of a number 
c = abs(-10.54)
print(c)

# Return the value to a power
d = pow(5, 2)
print(d)

# Extend the list of mathematical functions
import math

# Find the square root of a number 
e = math.sqrt(25)
print(e)

# Return the value of PI
f = math.pi
print(f)

# Round upwards and downwards to nearest integer
g = math.ceil(7.4)
h = math.floor(7.4)
print(g)
print(h)