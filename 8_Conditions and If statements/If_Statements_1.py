# Create an if statement
a = 45
b = 9200
if b > a:
  print("b is greater than a")

# Create an elif statement
a = 400
b = 400
if b > a:
  print("b is greater than a")
elif a == b:
  print("a and b are equal")

# Create and else statement
a = 100
b = 50
if b > a:
  print("b is greater than a")
elif a == b:
  print("a and b are equal")
else:
  print("a is greater than b")

# Create an else statement without elif
a = 100
b = 50
if b > a:
  print("b is greater than a")
else:
  print("a is greater than b")

# Create a Shorthand if statement 
if a > b: print("a is greater than b")

# Create a Shorthand if else statment
print("A") if a > b else print("B") 